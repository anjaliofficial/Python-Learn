#Data Types in Python 
# Numeric - int, float, complex
# Sequence Type - string, list, tuple
# Mapping Type - dict
# Boolean - bool
# Set Type - set, frozenset
# Binary Types - bytes, bytearray, memoryview

#Example of Numeric data types in python 
a = 5 # Int 
print(type(a))

b = 5.0 #Float
print(type(b))

c = 2+4j   # complex
print(type(c))